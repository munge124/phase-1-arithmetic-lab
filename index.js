// 1. Multiplication that equals 62
let num1 = 31;
let num2 = 2;
let multiply = num1 * num2;  // 31 * 2 should give 62

// 2. Random number greater than 0
let random = Math.floor(Math.random() * 100) + 1;  // Random integer between 1 and 100

// 3. Modulo operation to return remainder 4
let num3 = 24;
let num4 = 20;
let mod = num3 % num4;  // 24 % 20 should return 4

// 4. Max value
let max = Math.max(1, 5, 10, 20, 15);  // 20 is the maximum value

console.log(multiply);  // 62
console.log(random);    // Random number between 1 and 100
console.log(mod);       // 4
console.log(max);       // 20
